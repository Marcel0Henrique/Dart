void main(List<String> args) {
  //*Laço de repetição While
  /* 
     usada quando não sabemos quantas vezes um determinado bloco de instruções 
     precisa ser repetido. Com ele, a execução das instruções vai continuar até 
     que uma condição seja verdadeira. 
  */

  //? Exemplo
  int n = 0;

  while (n < 10) {
    print("Valor de n é ${n}");
    n++;
  }
}
