class Bola {
  //*Atributos
  String? _cor;
  double? _circunferencia;
  String? _material;

  //*Getters
  get cor => this._cor;
  get circunferencia => this._circunferencia;
  get material => this._material;

  //*Setters
  set cor(value) => this._cor = value;
  set circunferencia(value) => this._circunferencia = value;
  set material(value) => this._material = value;
}
