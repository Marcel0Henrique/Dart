void main(List<String> args) {
  //* Operadores Logicos
  /*  

    Maior que | >
    Maior ou igual | >=

    Menor que | <
    Menor ou igual | <=

    Igual | ==
    Diferente | !=

  */

  /*
    AND(E) | &&
    OR(OU) | ||
  */

  print(15 > 10);
  print(15 >= 15);
  print(30 < 20);
  print(35 <= 35);
  print(5 == 5);
  print(75 != 100);

  print("");

  print(10 < 20 && 20 > 10);
  print(35 > 10 || 20 < 10);
}
